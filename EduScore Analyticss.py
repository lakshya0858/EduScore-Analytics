print("\n--- Part 2: Classification Model (Mathematics Score Category Prediction) ---")
df_class = df_cleaned.copy()

bins = [0, 59, 79, 100]
labels = ['Low', 'Medium', 'High']
df_class['score_class'] = pd.cut(df_class['math_score'], bins=bins, labels=labels, include_lowest=True, right=True)

print("\nMathematics Score Class Distribution:")
print(df_class['score_class'].value_counts())

X_class_features = ['gender', 'race_ethnicity', 'parental_level_of_education', 'lunch', 'test_preparation_course']
X_class = df_class[X_class_features]
y_class = df_class['score_class']

categorical_transformer_class = Pipeline(steps=[
    ('onehot', OneHotEncoder(handle_unknown='ignore', drop='first'))
])

preprocessor_class = ColumnTransformer(
    transformers=[
        ('cat', categorical_transformer_class, X_class_features)
    ])

rf_class_pipeline = Pipeline(steps=[('preprocessor', preprocessor_class),
                                    ('classifier', RandomForestClassifier(random_state=42))])

X_train_class, X_test_class, y_train_class, y_test_class = train_test_split(
    X_class, y_class, test_size=0.2, random_state=42, stratify=y_class # stratify penting untuk target kelas
)

print("\nTraining Classification Model (Random Forest for score_class)...")
rf_class_pipeline.fit(X_train_class, y_train_class)

y_pred_class = rf_class_pipeline.predict(X_test_class)

print("\nClassification Report for 'score_class':")
print(classification_report(y_test_class, y_pred_class))

cm_class = confusion_matrix(y_test_class, y_pred_class, labels=rf_class_pipeline.named_steps['classifier'].classes_)
disp_class = ConfusionMatrixDisplay(confusion_matrix=cm_class, display_labels=rf_class_pipeline.named_steps['classifier'].classes_)
disp_class.plot(cmap='Blues')
plt.title('Confusion Matrix for score_class')
plt.show()

try:
    feature_names_out = rf_class_pipeline.named_steps['preprocessor'].get_feature_names_out()
    importances_class = rf_class_pipeline.named_steps['classifier'].feature_importances_

    feature_importance_df = pd.DataFrame({'feature': feature_names_out, 'importance': importances_class})
    feature_importance_df = feature_importance_df.sort_values(by='importance', ascending=False)

    plt.figure(figsize=(10, 8))
    sns.barplot(x='importance', y='feature', data=feature_importance_df.head(15), palette='viridis')
    plt.title('Random Forest - Feature Importance for score_class')
    plt.xlabel("Feature Importance")
    plt.ylabel("Feature")
    plt.tight_layout()
    plt.show()
except Exception as e:
    print(f"Unable to display feature importance: {e}")

