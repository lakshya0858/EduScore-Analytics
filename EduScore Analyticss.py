print("\n--- Part 4: Regression Model for Predicting Mathematics Score ---")
df_reg = df_cleaned.copy()

"""## Feature and Target Data"""

X_reg_categorical_features = ['gender', 'race_ethnicity', 'parental_level_of_education', 'lunch', 'test_preparation_course']
X_reg_numerical_features = ['reading_score', 'writing_score']
X_reg = df_reg[X_reg_categorical_features + X_reg_numerical_features]
y_reg = df_reg['math_score']

preprocessor_reg = ColumnTransformer(
    transformers=[
        ('cat', OneHotEncoder(handle_unknown='ignore', drop='first'), X_reg_categorical_features),
        ('num', StandardScaler(), X_reg_numerical_features)
    ],
    remainder='passthrough'
)

lr_reg_pipeline = Pipeline(steps=[('preprocessor', preprocessor_reg),
                                  ('regressor', LinearRegression())])

X_train_reg, X_test_reg, y_train_reg, y_test_reg = train_test_split(
    X_reg, y_reg, test_size=0.2, random_state=42
)

print("\nTraining Regression Model (Linear Regression for math_score)...")
lr_reg_pipeline.fit(X_train_reg, y_train_reg)

pickle.dump(lr_reg_pipeline, open("model.pkl", "wb"))

y_pred_reg_test = lr_reg_pipeline.predict(X_test_reg)

mse_reg = mean_squared_error(y_test_reg, y_pred_reg_test)
r2_reg = r2_score(y_test_reg, y_pred_reg_test)

print("\nEvaluasi Model Regresi pada Data Uji:")
print(f"Mean Squared Error (MSE): {mse_reg:.2f}")
print(f"R-squared (R2 Score): {r2_reg:.2f}")

results_reg_df = pd.DataFrame({'Actual Math Score': y_test_reg, 'Predicted Math Score': y_pred_reg_test})
